_ALERT_SYSTEM = (
    "你是一位A股盘中异动监控助手，负责从自选股实时数据中识别值得提醒的异动。"
    "只输出要求的JSON，不要输出任何解释。"
)

_ALERT_USER = """自选股实时数据
（price现价, change_pct涨跌幅%, volume今日成交量, volume_5d_avg五日日均成交量,
high_20d二十日高点, low_20d二十日低点）：

{rows_json}

请识别需要提醒的异动（如大涨大跌、明显放量、突破或跌破二十日高低点等）。
要求：
1. 只输出JSON数组：[{{"code":"...","name":"...","price":12.5,"change_pct":6.0,"reason":"中文一句话说明","level":"HIGH"}}]
2. level 只能是 HIGH（重大异动）或 MEDIUM（一般异动）
3. code 必须来自输入列表，不得编造
4. 没有异动则输出 []"""


def generate_alerts(
    rows: List[Dict[str, Any]],
    cfg: LLMConfig,
) -> Optional[List[Dict[str, Any]]]:
    """Ask the LLM to spot anomalies in watchlist rows.

    Returns alert dicts in the same shape as ``AlertEngine.process_alerts``
    (``timestamp`` left empty for the caller to stamp), or ``None`` on
    failure so the caller can fall back to the rule engine.  An empty list
    is a valid result meaning "no anomalies".
    """
    if not rows:
        return []
    try:
        user = _ALERT_USER.format(
            rows_json=json.dumps(
                [_compact_row(r) for r in rows], ensure_ascii=False, default=str
            ),
        )
        parsed = _extract_json(chat(cfg, _ALERT_SYSTEM, user))
        if not isinstance(parsed, list):
            raise LLMError("异动识别结果不是 JSON 数组")

        by_code = {str(r.get("code", "")): r for r in rows}
        alerts: List[Dict[str, Any]] = []
        for item in parsed:
            if not isinstance(item, dict):
                continue
            code = _resolve_code(item.get("code"), by_code)
            if code is None:
                continue
            reason = str(item.get("reason", "")).strip()
            if not reason:
                continue
            src = by_code[code]
            level = str(item.get("level", "MEDIUM")).upper()
            if level not in ("HIGH", "MEDIUM"):
                level = "MEDIUM"
            alerts.append({
                "code": code,
                "name": str(item.get("name") or src.get("name", "")),
                "price": _to_float(item.get("price", src.get("price"))),
                "change_pct": _to_float(
                    item.get("change_pct", src.get("change_pct"))
                ),
                "reason": reason,
                "level": level,
                "timestamp": "",
            })
        return alerts
    except LLMError as exc:
        logger.warning("LLM 异动识别失败: %s", exc)
        return None


