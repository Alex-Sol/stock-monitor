# Feature 1: 今日选股
# ---------------------------------------------------------------------------

_SELECT_SYSTEM = (
    "你是一位严谨的A股量化分析师，擅长结合技术面、成交量与板块热点选股。"
    "只输出要求的JSON，不要输出任何解释。"
)

_SELECT_USER = """以下是经过初步筛选的A股候选股票数据
（close现价, ma20二十日均线, volume_5d五日日均成交额, rsi14, macd_hist, pe市盈率,
high_20d/low_20d二十日高低点, change_20d二十日涨幅, sector板块, volume_today今日成交量）：

{rows_json}

今日板块涨幅排名（越靠前越热门）：{sector_rank}

请从中选出最值得关注的最多 {top_n} 只，按推荐程度从高到低排序。
要求：
1. 只输出JSON数组：[{{"code":"...","name":"...","score":85,"reason":"一句话中文理由"}}]
2. score 为 0-100 的整数，reason 不超过50字
3. code 必须来自输入列表，不得编造
4. 若没有值得推荐的，输出 []"""


def select_stocks(
    rows: List[Dict[str, Any]],
    cfg: LLMConfig,
    top_n: int = 5,
    sector_rank: Optional[List[str]] = None,
) -> Optional[List[Dict[str, Any]]]:
    """Ask the LLM to rank *rows*; return picked rows with score+reason.

    Each returned dict is the original row plus ``score`` (0-100) and a
    Chinese ``reason``.  Returns ``None`` on failure (caller falls back to
    the rule-based ``screen_stocks``).
    """
    if not rows:
        return []
    try:
        user = _SELECT_USER.format(
            rows_json=json.dumps(
                [_compact_row(r) for r in rows], ensure_ascii=False, default=str
            ),
            sector_rank="、".join(sector_rank) if sector_rank else "（无数据）",
            top_n=top_n,
        )
        parsed = _extract_json(chat(cfg, _SELECT_SYSTEM, user))
        if not isinstance(parsed, list):
            raise LLMError("选股结果不是 JSON 数组")

        by_code = {str(r.get("code", "")): r for r in rows}
        picked: List[Dict[str, Any]] = []
        for item in parsed:
            if not isinstance(item, dict):
                continue
            code = _resolve_code(item.get("code"), by_code)
            if code is None:
                continue
            picked.append({
                **by_code[code],
                "score": _to_float(item.get("score")),
                "reason": str(item.get("reason", "")),
            })
        picked.sort(key=lambda r: r["score"], reverse=True)
        return picked[:top_n]
    except LLMError as exc:
        logger.warning("LLM 选股失败: %s", exc)
        return None


