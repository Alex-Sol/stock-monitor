# Feature 3: 日报预览 / 收盘日报
# ---------------------------------------------------------------------------

_REPORT_SYSTEM = (
    "你是一位A股复盘分析师，负责撰写每日收盘日报，语气客观、数据准确。"
    "只输出要求的JSON，不要输出任何解释。"
)

_REPORT_USER = """今日日期：{report_date}

今日市场数据如下：

【大盘概况】{market_json}

【自选股表现】{watchlist_json}

【今日预警】{alerts_json}

【选股候选】{candidates_json}

请生成收盘日报，只输出如下JSON：
{{"title":"每日复盘","summary":"150字以内的中文复盘摘要","highlights":["要点1","要点2"],"markdown":"完整Markdown日报"}}

要求：
1. highlights 为 3-6 条一句话要点
2. markdown 包含 大盘概况 / 自选股表现 / 今日预警 / 明日关注 / 市场情绪 五个小节，中文撰写
3. 数据必须来自上述输入，不得编造"""


def generate_report(
    market: Dict[str, Any],
    watchlist_records: List[Dict[str, Any]],
    alert_records: List[Dict[str, Any]],
    candidate_records: List[Dict[str, Any]],
    cfg: LLMConfig,
    report_date: Optional[str] = None,
) -> Optional[Dict[str, Any]]:
    """Ask the LLM to write the daily report.

    Returns a dict with ``title`` / ``summary`` / ``highlights`` (for the
    frontend 日报预览 in daily_report.json) and ``markdown`` (full report),
    or ``None`` on failure so the caller falls back to the template report.
    ``report_date`` (e.g. ``"2026-07-18"``) is shown to the model so the
    report carries the correct date; defaults to today.
    """
    if not report_date:
        report_date = date.today().isoformat()
    try:
        user = _REPORT_USER.format(
            report_date=report_date,
            market_json=json.dumps(market, ensure_ascii=False, default=str),
            watchlist_json=json.dumps(
                [_compact_row(r) for r in watchlist_records],
                ensure_ascii=False, default=str,
            ),
            alerts_json=json.dumps(
                alert_records[-10:], ensure_ascii=False, default=str
            ),
            candidates_json=json.dumps(
                [_compact_row(r) for r in candidate_records[:5]],
                ensure_ascii=False, default=str,
            ),
        )
        parsed = _extract_json(chat(cfg, _REPORT_SYSTEM, user))
        if not isinstance(parsed, dict):
            raise LLMError("日报结果不是 JSON 对象")
        markdown = str(parsed.get("markdown", "")).strip()
        if not markdown:
            raise LLMError("日报 markdown 为空")
        highlights = parsed.get("highlights", [])
        if not isinstance(highlights, list):
            highlights = []
        return {
            "title": str(parsed.get("title", "每日复盘") or "每日复盘"),
            "summary": str(parsed.get("summary", "")),
            "highlights": [str(h) for h in highlights if h],
            "markdown": markdown,
        }
    except LLMError as exc:
        logger.warning("LLM 日报生成失败: %s", exc)
        return None


