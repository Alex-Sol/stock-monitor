"""LLM integration (OpenAI-compatible, e.g. Kimi/Moonshot) for the monitor.

When ``llm.enabled`` is true in config.json, three outputs are generated by
the configured model instead of the rule-based code:

- 今日选股 Top N   (select mode  -> candidates.json)
- 预警记录         (monitor mode -> alerts.json)
- 日报预览/收盘日报 (report mode  -> daily_report.json + report_*.md)

Every public function returns ``None`` on any failure (network, auth,
unparseable output) so callers can fall back to the rule-based path.
"""

from __future__ import annotations

import json
import logging
import re
from datetime import date
from typing import Any, Dict, List, Optional

import requests

from config import LLMConfig

logger = logging.getLogger(__name__)


class LLMError(Exception):
    """Raised when an LLM call or response parsing fails."""


# ---------------------------------------------------------------------------
# Low-level client
# ---------------------------------------------------------------------------

def chat(cfg: LLMConfig, system: str, user: str) -> str:
    """Send one system+user chat completion and return the reply text.

    Raises ``LLMError`` on missing credentials, HTTP errors, or malformed
    responses.
    """
    if not cfg.api_key:
        raise LLMError("未配置 llm.api_key")
    url = cfg.base_url.rstrip("/") + "/chat/completions"
    payload = {
        "model": cfg.model,
        "messages": [
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ],
        "temperature": cfg.temperature,
        # 结构化 JSON 输出不需要思考过程，关掉以节省 token、加快响应
        "thinking": {"type": "disabled"},
    }
    try:
        resp = requests.post(
            url,
            json=payload,
            headers={"Authorization": f"Bearer {cfg.api_key}"},
            timeout=cfg.timeout,
        )
        resp.raise_for_status()
        data = resp.json()
        return data["choices"][0]["message"]["content"]
    except requests.RequestException as exc:
        raise LLMError(f"LLM 请求失败: {exc}") from exc
    except (KeyError, IndexError, TypeError, ValueError) as exc:
        raise LLMError(f"LLM 响应格式异常: {exc}") from exc


def _extract_json(text: str) -> Any:
    """Parse JSON from an LLM reply, tolerating code fences and prose."""
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass
    m = re.search(r"```(?:json)?\s*(.*?)```", text, re.DOTALL)
    if m:
        try:
            return json.loads(m.group(1))
        except json.JSONDecodeError:
            pass
    for open_ch, close_ch in (("[", "]"), ("{", "}")):
        start = text.find(open_ch)
        end = text.rfind(close_ch)
        if start != -1 and end > start:
            try:
                return json.loads(text[start:end + 1])
            except json.JSONDecodeError:
                continue
    raise LLMError(f"无法从模型回复中解析 JSON: {text[:200]}")


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _compact_row(row: Dict[str, Any]) -> Dict[str, Any]:
    """Shrink one data row for the prompt: round floats, keep the rest.

    Strings (e.g. ``code="000001"``) are passed through untouched so
    leading zeros survive.
    """
    out: Dict[str, Any] = {}
    for k, v in row.items():
        if isinstance(v, str) or v is None:
            out[k] = v if v is not None else ""
            continue
        try:
            out[k] = round(float(v), 4)  # covers int/float/numpy numbers/bool
        except (TypeError, ValueError):
            out[k] = str(v)
    return out


def _to_float(value: Any, default: float = 0.0) -> float:
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def _resolve_code(raw: Any, known_codes: Dict[str, Dict[str, Any]]) -> Optional[str]:
    """Match an LLM-returned code to an input row, tolerating int coercion."""
    code = str(raw if raw is not None else "").strip()
    if code in known_codes:
        return code
    # e.g. model emitted 1 instead of "000001"
    if code.isdigit() and len(code) < 6:
        padded = code.zfill(6)
        if padded in known_codes:
            return padded
    return None


